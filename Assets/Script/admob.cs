    // reklamı görüntüleme butonları
    public void ShowAd()
    {
        if (interstitialAd != null && interstitialAd.CanShowAd())
        {
            interstitialAd.Show();
        }
    }


    public void ShowRewardedInterstitialAd()
    {
        const string rewardMsg =
            "Rewarded interstitial ad rewarded the user. Type: {0}, amount: {1}.";

        if (rewardedInterstitialAd != null && rewardedInterstitialAd.CanShowAd())
        {
            rewardedInterstitialAd.Show((Reward reward) =>
            {
                // coinManager.AddCoins(gameManager.earnedCoins);
                GameManager gameManager = GetComponent<GameManager>();
                gameManager.adsOpened = true;
                int earnedCoins = gameManager.earnedCoins;
                int newEarnedCoins = earnedCoins * 2;
                gameManager.earnedCoins = newEarnedCoins;
                gameManager.adstogivecoin();
                Debug.Log(String.Format(rewardMsg, reward.Type, reward.Amount));
            });
        }
    }

    public void ShowRewardedAd()
    {
